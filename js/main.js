  // IPv4 Subnet Calculator Math Helper
  function ip2int(ip) {
    return (
      ip.split(".").reduce((int, oct) => (int << 8) + parseInt(oct, 10), 0) >>>
      0
    );
  }
  function int2ip(int) {
    return [
      (int >>> 24) & 255,
      (int >>> 16) & 255,
      (int >>> 8) & 255,
      int & 255,
    ].join(".");
  }

  window.calculateIPv4 = () => {
    let ipInput = document.getElementById("ip-input").value.trim();
    let cidrInput = document.getElementById("cidr-input").value.trim();
    const output = document.getElementById("tool-output");

    try {
      if (!ipInput) throw new Error("Please enter an IP Address.");
      if (!cidrInput) cidrInput = "24"; // Default to /24

      // Automatically extract cidr if passed in the IP string (e.g. 192.168.1.1/24)
      if (ipInput.includes("/")) {
        const parts = ipInput.split("/");
        ipInput = parts[0];
        cidrInput = parts[1];
      }

      let cidr = parseInt(cidrInput);

      // If user inputted a subnet mask (e.g. 255.255.255.0) instead of CIDR
      if (cidrInput.includes(".")) {
        let maskInt = ip2int(cidrInput);
        let count = 0;
        while (maskInt > 0) {
          if (maskInt & 1) count++;
          maskInt >>>= 1;
        }
        cidr = count;
      }

      if (isNaN(cidr) || cidr < 0 || cidr > 32)
        throw new Error("Invalid CIDR mask. Must be 0-32.");

      const ipParts = ipInput.split(".");
      if (
        ipParts.length !== 4 ||
        ipParts.some(
          (p) => isNaN(parseInt(p)) || parseInt(p) < 0 || parseInt(p) > 255,
        )
      ) {
        throw new Error("Invalid IPv4 Address format.");
      }

      const ipInt = ip2int(ipInput);
      const maskInt = cidr === 0 ? 0 : (~0 << (32 - cidr)) >>> 0;
      const networkInt = (ipInt & maskInt) >>> 0;
      const broadcastInt = networkInt | (~maskInt >>> 0);

      const maxHosts = cidr >= 31 ? 0 : Math.pow(2, 32 - cidr) - 2;
      const wildcardInt = ~maskInt >>> 0;

      const network = int2ip(networkInt);
      const mask = int2ip(maskInt);
      const broadcast = int2ip(broadcastInt);
      const wildcard = int2ip(wildcardInt);

      let firstHost = "N/A",
        lastHost = "N/A";
      if (cidr <= 30) {
        firstHost = int2ip(networkInt + 1);
        lastHost = int2ip(broadcastInt - 1);
      }

      output.innerHTML = `
                <div style="text-align:left; font-size:14px; line-height:1.6;">
                    <strong style="color:var(--primary-color)">IP:</strong> ${ipInput} / ${cidr}<br>
                    <strong style="color:var(--primary-color)">SUBNET MASK:</strong> ${mask}<br>
                    <strong style="color:var(--primary-color)">NETWORK DIR:</strong> ${network}<br>
                    <strong style="color:var(--primary-color)">BROADCAST:</strong> ${broadcast}<br>
                    <hr style="border:0; border-top:1px solid #333; margin:10px 0;">
                    <strong style="color:var(--primary-color)">FIRST HOST:</strong> ${firstHost}<br>
                    <strong style="color:var(--primary-color)">LAST HOST:</strong> ${lastHost}<br>
                    <strong style="color:var(--primary-color)">MAX HOSTS:</strong> ${maxHosts.toLocaleString()}<br>
                    <strong style="color:var(--primary-color)">WILDCARD MASK:</strong> ${wildcard}
                </div>
            `;
    } catch (e) {
      output.innerText = `[ERROR]: ${e.message}`;
    }
  };

