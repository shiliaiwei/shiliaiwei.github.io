  window.doConvert = () => {
    const type = document.getElementById("conv-type").value;
    const input = document.getElementById("text-input").value.trim();
    const out = document.getElementById("tool-output");

    if (!input) {
      out.innerText = "PLEASE ENTER DATA TO CONVERT.";
      return;
    }

    try {
      let res = "";
      if (type === "text") {
        // Text to Bin, Hex, Dec
        let bin = "",
          hex = "",
          dec = "";
        for (let i = 0; i < input.length; i++) {
          let c = input.charCodeAt(i);
          bin += c.toString(2).padStart(8, "0") + " ";
          hex += c.toString(16).padStart(2, "0") + " ";
          dec += c.toString(10) + " ";
        }
        res = `BIN: ${bin.trim()}\nHEX: ${hex.trim()}\nDEC: ${dec.trim()}`;
      } else if (type === "binary") {
        // Bin to Text, Hex, Dec
        let chars = input.split(/\s+/);
        let text = "",
          hex = "",
          dec = "";
        for (let b of chars) {
          if (!/^[01]+$/.test(b)) throw new Error("Invalid Binary Input");
          let n = parseInt(b, 2);
          text += String.fromCharCode(n);
          hex += n.toString(16).padStart(2, "0") + " ";
          dec += n.toString(10) + " ";
        }
        res = `TXT: ${text}\nHEX: ${hex.trim()}\nDEC: ${dec.trim()}`;
      } else if (type === "hex") {
        let chars = input.replace(/0x/g, "").split(/\s+/);
        if (chars.length === 1 && input.length > 2 && !input.includes(" ")) {
          chars = input.match(/.{1,2}/g);
        }
        let text = "",
          bin = "",
          dec = "";
        for (let h of chars) {
          if (!/^[0-9a-fA-F]+$/.test(h)) throw new Error("Invalid Hex Input");
          let n = parseInt(h, 16);
          text += String.fromCharCode(n);
          bin += n.toString(2).padStart(8, "0") + " ";
          dec += n.toString(10) + " ";
        }
        res = `TXT: ${text}\nBIN: ${bin.trim()}\nDEC: ${dec.trim()}`;
      } else if (type === "decimal") {
        let chars = input.split(/\s|,/);
        let text = "",
          bin = "",
          hex = "";
        for (let d of chars) {
          if (!d.trim()) continue;
          let n = parseInt(d, 10);
          if (isNaN(n)) throw new Error("Invalid Decimal Input");
          text += String.fromCharCode(n);
          bin += n.toString(2).padStart(8, "0") + " ";
          hex += n.toString(16).padStart(2, "0") + " ";
        }
        res = `TXT: ${text}\nBIN: ${bin.trim()}\nHEX: ${hex.trim()}`;
      }

      out.innerText = res;
    } catch (e) {
      out.innerText = "[CONVERSION ERROR]: " + e.message;
    }
  };

