  const canvas = document.getElementById("spectrum-visualizer");
  const ctx = canvas ? canvas.getContext("2d") : null;

  function drawVisualizer() {
    if (!ctx || !canvas) return;
    const width = (canvas.width = canvas.offsetWidth);
    const height = (canvas.height = canvas.offsetHeight);
    ctx.clearRect(0, 0, width, height);

    const numBars = 30;
    const barWidth = width / numBars;

    for (let i = 0; i < numBars; i++) {
      let barHeight = 4;
      if (isPlaying) {
        // Simulate beat with random heights, scaled by sine wave for smoothness
        const time = Date.now() / 200;
        const noise = Math.random() * 0.5 + 0.5;
        const wave = Math.abs(Math.sin(time + i * 0.4));
        barHeight = 10 + (height - 20) * wave * noise;
      }

      // Draw bar
      const x = i * barWidth + barWidth * 0.2;
      const w = barWidth * 0.6;
      const y = height - barHeight;

      // Gradient
      const gradient = ctx.createLinearGradient(0, height, 0, 0);
      gradient.addColorStop(0, "#ff0050");
      gradient.addColorStop(1, "#ff6b3d");

      ctx.fillStyle = gradient;
      ctx.fillRect(x, y, w, barHeight);
    }

    if (isPlaying) {
      visualizerInterval = requestAnimationFrame(drawVisualizer);
    }
  }

  if (canvas) {
    drawVisualizer(); // Initial draw (flat)
  }

