  function initYTPlayer() {
    ytPlayer = new YT.Player("youtube-player", {
      height: "1",
      width: "1",
      playerVars: {
        playsinline: 1,
        listType: "playlist",
        list: currentPlaylistId,
        autoplay: 0,
        controls: 0,
        disablekb: 1,
      },
      events: {
        onReady: onPlayerReady,
        onStateChange: onPlayerStateChange,
      },
    });
  }

  if (window.YT && window.YT.Player) {
    initYTPlayer();
  } else {
    window.onYouTubeIframeAPIReady = initYTPlayer;
  }

  function onPlayerReady(event) {
    playlistList.innerHTML =
      '<div style="text-align:center; padding: 20px; color: var(--dim-color);">PLAYLIST SYNCHRONIZED.<br><br>PRESS PLAY TO INITIATE SEQUENCE.</div>';
    currentTitle.innerText = "READY // GYM";
    currentArtist.innerText = "YOUTUBE PROTOCOL";
  }

  function onPlayerStateChange(event) {
    if (event.data == YT.PlayerState.PLAYING) {
      isPlaying = true;
      playIcon.style.display = "none";
      pauseIcon.style.display = "block";
      if (playerArea) playerArea.classList.add("playing");

      // Extract title if possible
      const videoData = ytPlayer.getVideoData();
      if (videoData && videoData.title) {
        currentTitle.innerText = videoData.title;
      }

      if (!visualizerInterval) {
        drawVisualizer();
      }

      startProgressTracking();
    } else {
      isPlaying = false;
      playIcon.style.display = "block";
      pauseIcon.style.display = "none";
      if (playerArea) playerArea.classList.remove("playing");
      cancelAnimationFrame(visualizerInterval);
      visualizerInterval = null;
      drawVisualizer(); // draw flat
      stopProgressTracking();
    }
  }

  function togglePlay() {
    if (!ytPlayer || !ytPlayer.getPlayerState) return;
    const state = ytPlayer.getPlayerState();
    if (state === YT.PlayerState.PLAYING) {
      ytPlayer.pauseVideo();
    } else {
      ytPlayer.playVideo();
    }
  }

  function prevSong() {
    if (ytPlayer && ytPlayer.previousVideo) ytPlayer.previousVideo();
  }

  function nextSong() {
    if (ytPlayer && ytPlayer.nextVideo) ytPlayer.nextVideo();
  }

  if (playBtn) playBtn.addEventListener("click", togglePlay);
  if (prevBtn) prevBtn.addEventListener("click", prevSong);
  if (nextBtn) nextBtn.addEventListener("click", nextSong);

